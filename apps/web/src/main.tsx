export function DocumentApp() {
  const [documents, setDocuments] = useState<Document[]>(seededDocuments);
  const [meta, setMeta] = useState<DocumentMetaResponse>({
    documentTypes: [...documentTypeSchema.options],
    documentStatuses: [...documentStatusSchema.options],
  });
  const [selectedDocumentId, setSelectedDocumentId] = useState<string>(seededDocuments[0]?.id ?? '');
  const [state, setState] = useState<AsyncState>('idle');
  const [message, setMessage] = useState<string>('Loading document workspace...');
  const [view, setView] = useState<AppView>('dashboard');
  const [selectedType, setSelectedType] = useState<'all' | DocumentType>('all');
  const [searchTerm, setSearchTerm] = useState('');
  const [editorMode, setEditorMode] = useState<EditorMode>('create');
  const [draft, setDraft] = useState<EditorDraft>(defaultEditorDraft);
  const [findings, setFindings] = useState<Finding[]>(seededFindings.filter((finding) => finding.documentId === selectedDocumentId));
  const [traceEvents, setTraceEvents] = useState<TraceEvent[]>(seededTraceEvents.filter((event) => event.documentId === selectedDocumentId));
  const [chatMessages, setChatMessages] = useState<ChatMessage[]>(seededChatMessages.filter((message) => message.documentId === selectedDocumentId));
  const [chatDraft, setChatDraft] = useState('What is the next priority here?');

  useEffect(() => {
    async function load() {
      setState('loading');
      try {
        const [documentResponse, metaResponse] = await Promise.all([
          fetchJson('/api/documents', undefined, documentListResponseSchema),
          fetchJson('/api/meta/document-types', undefined, documentMetaResponseSchema),
        ]);

        setDocuments(documentResponse.documents);
        setMeta(metaResponse);
        setSelectedDocumentId((current) => current || documentResponse.documents[0]?.id || '');
        setMessage(`Loaded ${documentResponse.total} documents from the API.`);
        setState('ready');
      } catch {
        setMessage('API unavailable. Rendering bundled seed documents while backend comes online.');
        setState('error');
      }
    }

    void load();
  }, []);

  const filteredDocuments = useMemo(() => {
    const normalizedSearch = searchTerm.trim().toLowerCase();
    return documents.filter((document) => {
      const matchesType = selectedType === 'all' || document.type === selectedType;
      const haystack = `${document.title} ${document.summary} ${document.tags.join(' ')}`.toLowerCase();
      const matchesSearch = normalizedSearch.length === 0 || haystack.includes(normalizedSearch);
      return matchesType && matchesSearch;
    });
  }, [documents, searchTerm, selectedType]);

  const selectedDocument = useMemo(
    () => documents.find((document) => document.id === selectedDocumentId) ?? filteredDocuments[0] ?? documents[0],
    [documents, filteredDocuments, selectedDocumentId],
  );

  useEffect(() => {
    if (selectedDocument && selectedDocument.id !== selectedDocumentId) {
      setSelectedDocumentId(selectedDocument.id);
    }
  }, [selectedDocument, selectedDocumentId]);

  useEffect(() => {
    if (!selectedDocumentId) {
      return;
    }

    async function loadParity() {
      try {
        const [findingsResponse, traceResponse, chatResponse] = await Promise.all([
          fetchJson(`/api/documents/${selectedDocumentId}/findings`, undefined, documentFindingsResponseSchema),
          fetchJson(`/api/documents/${selectedDocumentId}/trace`, undefined, documentTraceResponseSchema),
          fetchJson(`/api/documents/${selectedDocumentId}/chat`, undefined, documentChatResponseSchema),
        ]);

        setFindings(findingsResponse.findings);
        setTraceEvents(traceResponse.events);
        setChatMessages(chatResponse.messages);
      } catch {
        setFindings(seededFindings.filter((finding) => finding.documentId === selectedDocumentId));
        setTraceEvents(seededTraceEvents.filter((event) => event.documentId === selectedDocumentId));
        setChatMessages(seededChatMessages.filter((message) => message.documentId === selectedDocumentId));
      }
    }

    void loadParity();
  }, [selectedDocumentId]);

  function openCreateEditor() {
    setEditorMode('create');
    setDraft(defaultEditorDraft);
    setView('editor');
  }

  function openEditEditor() {
    setEditorMode('edit');
    setDraft(toEditorDraft(selectedDocument));
    setView('editor');
  }

  async function handleCreateSeedDocument() {
    const payload = createSeedPayload(documents);

    try {
      const created = await fetchJson('/api/documents', {
        method: 'POST',
        body: JSON.stringify(payload),
      }, documentSchema);

      setDocuments((current) => [created, ...current]);
      setSelectedDocumentId(created.id);
      setState('ready');
      setMessage(`Created ${created.type} document “${created.title}”.`);
      setView('documents');
    } catch {
      const fallback = createLocalDocument(payload, documents);
      setDocuments((current) => [fallback, ...current]);
      setSelectedDocumentId(fallback.id);
      setState('error');
      setMessage(`API unavailable. Added local fallback document “${fallback.title}”.`);
      setView('documents');
    }
  }

  async function handleSaveDraft() {
    if (editorMode === 'create') {
      const payload = draftToCreatePayload(draft);

      try {
        const created = await fetchJson('/api/documents', {
          method: 'POST',
          body: JSON.stringify(payload),
        }, documentSchema);

        setDocuments((current) => [created, ...current]);
        setSelectedDocumentId(created.id);
        setState('ready');
        setMessage(`Saved new ${created.type} document “${created.title}”.`);
      } catch {
        const fallback = createLocalDocument(payload, documents);
        setDocuments((current) => [fallback, ...current]);
        setSelectedDocumentId(fallback.id);
        setState('error');
        setMessage(`API unavailable. Added local draft document “${fallback.title}”.`);
      }

      setView('documents');
      return;
    }

    if (!selectedDocument) {
      return;
    }

    const payload = draftToUpdatePayload(draft);

    try {
      const updated = await fetchJson(`/api/documents/${selectedDocument.id}`, {
        method: 'PATCH',
        body: JSON.stringify(payload),
      }, documentSchema);

      setDocuments((current) => current.map((document) => document.id === updated.id ? updated : document));
      setSelectedDocumentId(updated.id);
      setState('ready');
      setMessage(`Updated document “${updated.title}”.`);
    } catch {
      const fallback = updateLocalDocument(selectedDocument, payload);
      setDocuments((current) => current.map((document) => document.id === fallback.id ? fallback : document));
      setSelectedDocumentId(fallback.id);
      setState('error');
      setMessage(`API unavailable. Saved local edits for “${fallback.title}”.`);
    }

    setView('documents');
  }

  async function handleSendParityChat() {
    if (!selectedDocument) {
      return;
    }

    const payload = createChatMessageInputSchema.parse({
      documentId: selectedDocument.id,
      content: chatDraft.trim(),
    });

    try {
      const response = await fetchJson(`/api/documents/${selectedDocument.id}/chat`, {
        method: 'POST',
        body: JSON.stringify({ content: payload.content }),
      }, documentChatResponseSchema);

      setChatMessages((current) => [...current, ...response.messages]);
      setMessage(`Parity assistant responded for “${selectedDocument.title}”.`);
      setState('ready');
    } catch {
      const fallback = createLocalParityReply(selectedDocument.id, payload.content, chatMessages);
      setChatMessages((current) => [...current, ...fallback]);
      setMessage(`API unavailable. Added local parity response for “${selectedDocument.title}”.`);
      setState('error');
    }

    setChatDraft('');
  }

  return (
    <main style={{ fontFamily: 'Inter, sans-serif', padding: 24, color: '#111827', background: '#f9fafb', minHeight: '100vh' }}>
      <header style={{ marginBottom: 24 }}>
        <h1 style={{ marginBottom: 8 }}>SuperShip parity workspace</h1>
        <p style={{ margin: 0 }}>Phase 5 adds proactive findings, traceability, and on-demand parity chat on top of the unified document model.</p>
      </header>

      <nav style={{ display: 'flex', gap: 12, flexWrap: 'wrap', marginBottom: 16 }}>
        <NavigationTab label="Dashboard" active={view === 'dashboard'} onClick={() => setView('dashboard')} />
        <NavigationTab label="Documents" active={view === 'documents'} onClick={() => setView('documents')} />
        <NavigationTab label="Editor" active={view === 'editor'} onClick={() => setView('editor')} />
        <NavigationTab label="Parity" active={view === 'parity'} onClick={() => setView('parity')} />
        <button onClick={() => void handleCreateSeedDocument()} style={{ padding: '10px 14px', borderRadius: 10, border: '1px solid #111827', background: '#111827', color: '#ffffff', marginLeft: 'auto' }}>
          Create seed document
        </button>
      </nav>

      <section style={{ marginBottom: 16 }}>
        <p>{message}</p>
      </section>

      {view === 'dashboard' ? (
        <DashboardView
          documents={documents}
          state={state}
          onOpenDocuments={() => setView('documents')}
          onOpenCreate={openCreateEditor}
          onOpenParity={() => setView('parity')}
        />
      ) : null}

      {view === 'documents' ? (
        <DocumentsView
          filteredDocuments={filteredDocuments}
          selectedDocument={selectedDocument}
          selectedType={selectedType}
          searchTerm={searchTerm}
          meta={meta}
          onSearch={setSearchTerm}
          onSelectType={setSelectedType}
          onSelectDocument={setSelectedDocumentId}
          onEdit={openEditEditor}
        />
      ) : null}

      {view === 'editor' ? (
        <EditorView
          draft={draft}
          mode={editorMode}
          meta={meta}
          activeDocument={selectedDocument}
          onChange={(patch) => setDraft((current) => ({ ...current, ...patch }))}
          onSave={() => void handleSaveDraft()}
        />
      ) : null}

      {view === 'parity' ? (
        <ParityView
          document={selectedDocument}
          findings={findings}
          traceEvents={traceEvents}
          chatMessages={chatMessages}
          chatDraft={chatDraft}
          onChatDraftChange={setChatDraft}
          onSendChat={() => void handleSendParityChat()}
        />
      ) : null}
    </main>
  );
}

