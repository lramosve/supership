type EditorDraft = {
  type: DocumentType;
  title: string;
  summary: string;
  content: string;
  status: DocumentStatus;
  ownerId: string;
  tags: string;
};

const apiBaseUrl = (globalThis as { __SUPERSHIP_API_BASE_URL__?: string }).__SUPERSHIP_API_BASE_URL__ ?? 'http://localhost:3000';

const defaultEditorDraft: EditorDraft = {
  type: 'wiki',
  title: '',
  summary: '',
  content: '',
  status: 'draft',
  ownerId: 'person-web-shell',
  tags: 'seed, web',
};

function toEditorDraft(document?: Document): EditorDraft {
  if (!document) {
    return defaultEditorDraft;
  }

  return {
    type: document.type,
    title: document.title,
    summary: document.summary,
    content: document.content,
    status: document.status,
    ownerId: document.ownerId,
    tags: document.tags.join(', '),
  };
}

function draftToCreatePayload(draft: EditorDraft): CreateDocumentInput {
  return createDocumentInputSchema.parse({
    type: draft.type,
    title: draft.title.trim(),
    summary: draft.summary.trim(),
    content: draft.content,
    status: draft.status,
    ownerId: draft.ownerId.trim(),
    tags: draft.tags.split(',').map((tag) => tag.trim()).filter(Boolean),
  });
}

function draftToUpdatePayload(draft: EditorDraft): UpdateDocumentInput {
  return updateDocumentInputSchema.parse({
    type: draft.type,
    title: draft.title.trim(),
    summary: draft.summary.trim(),
    content: draft.content,
    status: draft.status,
    ownerId: draft.ownerId.trim(),
    tags: draft.tags.split(',').map((tag) => tag.trim()).filter(Boolean),
  });
}

async function fetchJson<T>(path: string, init?: RequestInit, schema?: { parse: (value: unknown) => T }): Promise<T> {
  const response = await fetch(`${apiBaseUrl}${path}`, {
    headers: {
      'Content-Type': 'application/json',
      ...(init?.headers ?? {}),
    },
    ...init,
  });

  if (!response.ok) {
    throw new Error(`Request failed with status ${response.status}`);
  }

  const json = await response.json();
  return schema ? schema.parse(json) : (json as T);
}

function createSeedPayload(existingDocuments: Document[]): CreateDocumentInput {
  const nextIndex = existingDocuments.length + 1;
  return createDocumentInputSchema.parse({
    type: nextIndex % 2 === 0 ? 'issue' : 'wiki',
    title: `Seeded document ${nextIndex}`,
    summary: `Autogenerated seed document ${nextIndex} for the SuperShip shell.`,
    content: `This document was created from the web shell to exercise the unified document workflow (${nextIndex}).`,
    status: 'draft',
    ownerId: 'person-web-shell',
    tags: ['seed', 'web'],
  });
}

function createLocalDocument(payload: CreateDocumentInput, existingDocuments: Document[]): Document {
  const timestamp = new Date().toISOString();
  return documentSchema.parse({
    id: `local-${existingDocuments.length + 1}`,
    ...payload,
    createdAt: timestamp,
    updatedAt: timestamp,
  });
}

function updateLocalDocument(existing: Document, payload: UpdateDocumentInput): Document {
  return documentSchema.parse({
    ...existing,
    ...payload,
    updatedAt: new Date().toISOString(),
  });
}

function createLocalParityReply(documentId: string, prompt: string, existingMessages: ChatMessage[]): ChatMessage[] {
  const timestamp = new Date().toISOString();
  return [
    {
      id: `local-user-${existingMessages.length + 1}`,
      documentId,
      role: 'user',
      content: prompt,
      createdAt: timestamp,
    },
    {
      id: `local-assistant-${existingMessages.length + 2}`,
      documentId,
      role: 'assistant',
      content: 'Local parity assistant: review open findings, recent trace events, and the current document summary before choosing the next action.',
      createdAt: new Date(Date.now() + 1000).toISOString(),
    },
  ];
}

